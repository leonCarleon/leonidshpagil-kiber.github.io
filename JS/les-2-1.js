var animals = ['Кот','Сабака','Карова','Риба']
alert(animals)

// .push добовляет элемент в КОНЕЦ массива
animals.push('Лошать')
alert(animals)

// .unshift - добовляет элемент в НАЧАЛО массива
animals.unshift('Гузь')
alert(animals)

// .pop - удаляет ПОСЛЕДНИЙ элемент в массиве
animals.pop()
alert(animals)

// .shift - удаляет ПЕРВЫЙ элемент в массиве
animals.shift
alert(animals)